class IndianRupee{

private int indianRupeeId;
private int rupeeName;
private String printDate;
private String color;
private int serialNo;
private int noOfLanguages;
private int size;
private String location;

public void setIndianRupeeId(int indianRupeeId){
	this.indianRupeeId=indianRupeeId;
}
public int getIndianRupeeId(){
return indianRupeeId;
}

public void setRupeeName(int rupeeName){
	this.rupeeName=rupeeName;
	
}
public int getRupeeName(){
	return rupeeName;
	
}
public void setPrintDate(String printDate){
	this.printDate= printDate;
}
public String getPrintDate(){
	return printDate;
}
public void setColor(String color){
	this.color=color;
}
public String getColor(){
	return color;
	
}
public void setSerialNo(int serialNo){
	this.serialNo=serialNo;
}
public int getSerialNo(){
	return serialNo;
}
public void setNoOfLanguages(int noOfLanguages){
	this.noOfLanguages=noOfLanguages;
}
public long getNoOfLanguages(){
	return noOfLanguages;
}
public void setSize(int size){
	this.size=size;
}
public int getSize(){
	return size;
}
public void setLocation(String location){
	this.location=location;
}
public String getLocation(){
	return location;
}
 
 
}